/**
 * This package contains iterators over node trees.
 * <p>
 * It is often required to traverse a tree in a certain way (depth-first, breath-first, etc.).
 * In most cases, traversal is done only once and there is no need to store nodes in a collection.
 * Therefore, {@code Iterator} implementations are provided.
 * Also, to facilitate use of iterators, related {@code Iterable} implementations are provided.
 * They can be used in for loops and utility additional methods to deal with iterators (e.g.
 * dumping items to a collection if needed).
 *
 * @since 2020/11/19
 */
package org.cqfn.patternika.ast.iterator;
